class Skybox
{

    unsigned int VAO, VBO;
    unsigned int cubeMap;
    ShaderProgram shader;

    mat4 projection;
    mat4 view;

    public:
        Skybox(const std::vector<std::string> textures,const ShaderProgram& shaderProgram)
        {
            shader = shaderProgram;
            glGenVertexArrays(1, &VAO);
            glGenBuffers(1, &VBO);
            glBindVertexArray(VAO);
            glBindBuffer(GL_ARRAY_BUFFER, VBO);
            glBufferData(GL_ARRAY_BUFFER, sizeof(skyboxVertices), &skyboxVertices, GL_STATIC_DRAW);
            glEnableVertexAttribArray(0);
            glVertexAttribPointer(0, 3, GL_FLOAT, GL_FALSE, 3 * sizeof(float), (void*)0);
            shader.StartUseShader();
            shader.SetUniform("cubemap", 0);
            shader.StopUseShader();
            cubeMap = loadCubeMap(textures);
        }


        unsigned int loadCubeMap (const std::vector<std::string> textures);
        void drawSkybox(const mat4& projection, const mat4& view);
        void bindTexture()
        {
            glActiveTexture(GL_TEXTURE0);
            glBindTexture(GL_TEXTURE_CUBE_MAP, cubeMap);
        }

        void cleanUp()
        {
            glDeleteBuffers(1, &VBO);
            glDeleteVertexArrays(1, &VAO);
        }
};
